# Repetições com 'while'

while True:           # enquanto True for verdadeiro, faça:
    print('Olá')      # imprima 'Olá'
    break             # pare o loop

# O código acima se repete infinitamente, pois True sempre é verdadeiro.
# Para parar o loop, basta usar o comando 'break'.

while False:          # enquanto False for falso, faça:
    print('Olá')      # imprima 'Olá'
    break             # pare o loop

# O código acima não será executado porque sempre será False.
