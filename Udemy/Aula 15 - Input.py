# A função input() é usada para obter dados do usuário.
#
# Exemplo:
#
nome = input('Digite seu nome: ') # Digite seu nome: João
print(f'Olá, {nome}!') # Olá, João!

num1 = int(input(f'Digite um número: ')) # Digite um número: 10
num2 = int(input(f'Digite outro número: ')) # Digite outro número: 5
print(f'A soma é {num1 + num2}') # A soma é 15
