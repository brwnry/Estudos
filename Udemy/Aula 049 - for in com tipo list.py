# For in com listas:

# Exemplo:

lista = ['Maria', 'João', 'Pedro', 'Ana'] # Lista de nomes
for nome in lista: # Para cada nome na lista, faça:
    print(nome) # Imprima o nome

# Exemplo diferente:

lista = ['Maria', 'João', 'Pedro', 'Ana']
for nome in lista: # Para cada nome na lista, faça:
    print(f'Olá {nome}') # Imprima 'Olá' seguido do nome

