# Operadores de atribuição com operadores aritméticos.

a = 10 # int

a = a + 5 # 15
# Concatena a string com o valor de a.

a += 5 # 15
# Atribui o valor de a + 5 a a.

a -= 5 # 5
# Atribui o valor de a - 5 a a.

a *= 5 # 50
# Atribui o valor de a * 5 a a.

a /= 5 # 2.0
# Atribui o valor de a / 5 a a.

a %= 5 # 0
# Atribui o valor de a % 5 a a.

a //= 5 # 2
# Atribui o valor de a // 5 a a.

a **= 5 # 100000
# Atribui o valor de a ** 5 a a.

print(a)