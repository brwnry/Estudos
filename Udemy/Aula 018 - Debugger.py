# Debugger é um programa que permite visualizar o código em execução.
# Executa cada passo do código para entender o processo de execução. 
# 
# Exemplo:
#
x = 10                          # x recebe 10
y = 5                           # y recebe 5
#
if x > y:                       # Se x for maior que y
  print('x é maior que y')      # Exibe 'x é maior que y'
#
# O debugger pode ser acessado pelo botão de ferramentas no topo da página.
