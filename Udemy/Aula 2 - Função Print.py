# A função print() é uma função incorporada do Python, que imprime itens na tela.
# A função print() pode receber vários argumentos separados por vírgula.
# A função sep=' ' é um parâmetro opcional que define o separador entre os argumentos.
# A função \n é um parâmetro opcional que define quebra de linha.
# A função end='' é um parâmetro opcional que define o que deve ser impresso 
# no final da linha.

print(12, 34, sep="-") # 12-34
print(56, 78) # 56 78
      
