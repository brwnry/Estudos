# O tipo int é um número inteiro, positivo ou negativo.
# O tipo float é um número com ponto flutuante.

print(11) # int
print(0) # int
print(-34) # int


print(1.1, 10.95, -34.45) # float
print(type(6.4766)) # float

print(type(8846), type(75.74), type('Bruno')) # int, float, str
print(type(True), type(False)) # bool



      

