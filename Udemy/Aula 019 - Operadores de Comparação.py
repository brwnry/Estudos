# Operadores de comparação são usados para comparar valores.
#
# == Igual
# != Diferente
# > Maior que
# < Menor que
# >= Maior ou igual que
# <= Menor ou igual que
#
# Exemplos:
#
print(10 == 10)      # True
print(10 != 10)      # False
print(10 > 10)       # False
print(10 < 10)       # False
print(10 >= 10)      # True
print(10 <= 10)      # True