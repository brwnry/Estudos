# Concatenação é o ato de juntar dois valores.
# Exemplos:
#
print('10' + '5')                                 # 105
print('João' + 'Silva')                           # JoãoSilva
print('João' + ' ' + 'Silva')                     # João Silva
print('João' + ' ' + 'Silva' + ' ' + 'Sousa')     # João Silva Sousa

concatenacao = 'A' + 'B' + 'C'                    # ABC
print(concatenacao)                               # ABC

# Repetição é o ato de repetir um valor.

x10 = 'X' * 10                                    # XXXXXXXXXX
luis3 = '3' * 'luis'                              # luisluisluis
print(x10)                                        # XXXXXXXXXX
print(luis3 )                                     # luisluisluis