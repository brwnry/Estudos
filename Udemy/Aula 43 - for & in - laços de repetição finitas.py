# Laços de repetições finitas com 'for' e 'in'

# Criar uma variável para armazenar a quantidade de letras
texto = 'Python' # string

# Criar um laço de repetição para percorrer a frase
for letra in texto: # para cada letra na string texto, faça:
  
  # Mostrar a quantidade de letras
  print(letra) # imprima letra

