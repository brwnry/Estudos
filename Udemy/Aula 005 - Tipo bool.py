# O tipo bool é um tipo de dados que representa apenas dois valores: True ou False.
# O tipo bool é sempre um tipo de dados primitivo.

# Existem vários operadores lógicos que podem ser usados para comparar valores. 
# Dentre eles, o == (igualdade) , != (diferente), > (maior), < (menor) ,
# >=(maior igual), <= (menor igual) são alguns operadores lógicos.

print(10 == 10) # True
print(10 != 10) # False
print(10 > 10) # False
print(10 < 10) # False
print(10 >= 10) # True
print(10 <= 10) # True

