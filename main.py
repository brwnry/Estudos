nome = 'Olá mundo'     # string
print(nome[0:4])       # Olá
print(nome[4:8])       # mundo
print(nome[4:])        # mundo
print(nome[:4])        # Olá
print(nome[:])         # Olá mundo
print(nome[0:8:2])     # O l m d
print(nome[::2])       # O l m d
print(nome[::-1])      # odnum aló
print(nome[::-2])      # odnu ol
print(nome[::-3])      # odno